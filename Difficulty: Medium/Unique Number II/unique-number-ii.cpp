class Solution {
  public:
    vector<int> singleNum(vector<int>& arr) {
        // Code here.
        set<int> st;
        for(auto i:arr){
            if(st.find(i) == st.end()) st.insert(i);
            else st.erase(i);
        }
        
        vector<int> ans;
        for(auto i:st) ans.push_back(i);
        return ans;
    }
};


